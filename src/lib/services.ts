import { MegaMenuItem } from "@/components/ui/mega-menu";

// Функция для извлечения всех услуг из меню
export function getAllServices(navItems: MegaMenuItem[]): string[] {
  const services: string[] = [];
  
  // Находим пункт меню "Услуги"
  const servicesMenu = navItems.find(item => item.label === "Услуги");
  
  if (servicesMenu && servicesMenu.subMenus) {
    // Проходим по всем подменю
    servicesMenu.subMenus.forEach(subMenu => {
      if (subMenu.items) {
        // Добавляем все услуги из подменю
        subMenu.items.forEach(item => {
          if (item.label && !services.includes(item.label)) {
            services.push(item.label);
          }
        });
      }
    });
  }
  
  return services.sort();
}

